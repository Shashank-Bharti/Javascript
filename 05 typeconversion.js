// Convert to Boolean
    // Any non-empty string is true.
    // An empty string is false.
    // The number 0 is false.
    // Any other number is true.

    const str1 = "Hello"
    const str2 = ""
    const str3 = 0
    const str4 = 4
    console.log(Boolean(str1));
    console.log(Boolean(str2));
    console.log(Boolean(str3));
    console.log(Boolean(str4));

    
    
    
    

