
// 1. Strings
// A string is any set of characters enclosed in quotes, either single (') or double ("), or even backticks (`)
let userName = "Shashank";
console.log(typeof userName);

// 2. Numbers
// In JavaScript, numbers can be integers or floating-point (decimals).
const num = 12.36
console.log(typeof num);

// 3. Boolean
let _isValid = true;
console.log(typeof _isValid);

// 4. Undefined and Null
let username;
console.log(typeof username);

let user = null;
console.log(user);
console.log(typeof user);

// 5. Objects (kind of dicts in python)
const person = {
    name : "Shashank",
    age : 21,
    education : "UnderGraduate"
};
console.log(typeof person);

// 6. Arrays (lists in python)
const myList = [1,2,3,4,5,6];
console.log(typeof myList);
